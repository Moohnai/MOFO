            # # then we find the indices of the instances that have less than the maximum number of 1s
            # x_patch_yabide_less_idx = torch.nonzero(x_patch_yabide_less).flatten()
            # # then we find the number of 1s that we need to add to each instance
            # x_patch_yabide_add = x_patch_yabide_max - x_patch_yabide_count[x_patch_yabide_less_idx]
            # for i in range(x_patch_yabide_add.shape[0]):
